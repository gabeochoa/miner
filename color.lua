Color = Object:extend()
function Color:new(r, g, b, a)
    self.r = r
    self.b = b
    self.g = g
    self.a = a or 255
end

function Color:to01(a)
    return {
        r = self.r / 255,
        g = self.g / 255,
        b = self.b / 255,
        a = (a or self.a) / 255,
        __tostring = function()
            return "color(" .. self.r .. "," .. self.g .. "," .. self.b .. "," .. self.a .. ")"
        end
    }
end
function Color:__tostring()
    return "color(" .. self.r .. "," .. self.g .. "," .. self.b .. "," .. self.a .. ")"
end

